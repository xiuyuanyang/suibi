package beans;

public class ElectionMode {
	
	private String fangshi;
	private String miaoshu;
	private int tianshu;
	private int beixuanshu;
	
	public String getFangshi() {
		return fangshi;
	}
	public void setFangshi(String fangshi) {
		this.fangshi = fangshi;
	}
	public String getMiaoshu() {
		return miaoshu;
	}
	public void setMiaoshu(String miaoshu) {
		this.miaoshu = miaoshu;
	}
	public int getTianshu() {
		return tianshu;
	}
	public void setTianshu(int tianshu) {
		this.tianshu = tianshu;
	}
	public int getBeixuanshu() {
		return beixuanshu;
	}
	public void setBeixuanshu(int beixuanshu) {
		this.beixuanshu = beixuanshu;
	}
	
}
